import { useState } from "react";
import Footer from "./Footer";
import Header from "./Header";
import ImagePopup from "./ImagePopup";
import Main from "./Main";
import PopupWithForm from "./PopupWithForm";

function App() {
	const [isEditProfileOpen, setIsEditProfileOpen] = useState(false);
	const [isEditAvatarOpen, setIsEditAvatarOpen] = useState(false);
	const [isAddplaceOpen, setIsAddPlaceOpen] = useState(false);
	const [isConfirmDeleteOpen, setIsConfirmDeleteOpen] = useState(false);
	const [selectedCard, setSelectedCard] = useState(null);

	function handleCardClick(card) {
		setSelectedCard(card);
	}

	function handleEditProfileClick() {
		setIsEditProfileOpen(true);
	}
	function handleEditAvatarClick() {
		setIsEditAvatarOpen(true);
	}

	function closeAllPopups() {
		setIsEditProfileOpen(false);
		setIsEditAvatarOpen(false);
		setIsAddPlaceOpen(false);
		setIsConfirmDeleteOpen(false);
		setSelectedCard(null);
	}

	function handleAddPlaceClick() {
		setIsAddPlaceOpen(true);
	}

	function handleDeletePlaceClick() {
		setIsConfirmDeleteOpen(true);
	}

	return (
		<>
			<div className='page__container'>
				<Header />
				<Main
					onEditProfileClick={handleEditProfileClick}
					onEditAvatarClick={handleEditAvatarClick}
					onAddPlaceClick={handleAddPlaceClick}
					onDeletePlaceClick={handleDeletePlaceClick}
					onCardClick={handleCardClick}
				/>
				<Footer />
			</div>

			<PopupWithForm
				title='Редактировать профиль'
				name='edit-profile'
				isOpen={isEditProfileOpen}
				onClose={closeAllPopups}
			>
				<label className='form__field'>
					<input
						type='text'
						className='form__input'
						id='name-input'
						name='name'
						defaultValue={"Жак-Ив Кусто"}
						minLength='2'
						maxLength='40'
						required
					/>
					<span className='form__error name-input-error'></span>
				</label>
				<label className='form__field'>
					<input
						type='text'
						className='form__input'
						id='description-input'
						name='description'
						defaultValue={"Исследователь океана"}
						minLength='2'
						maxLength='200'
						required
					/>
					<span className='form__error description-input-error'></span>
				</label>
				<button type='submit' className='button form__submit-button'>
					Сохранить
				</button>
			</PopupWithForm>

			<PopupWithForm
				title='Обновить аватар'
				name='edit-avatar'
				isOpen={isEditAvatarOpen}
				onClose={closeAllPopups}
			>
				<label className='form__field'>
					<input
						type='url'
						className='form__input'
						id='link-input-avatar'
						name='link'
						placeholder='Ссылка на картинку'
						required
					/>
					<span className='form__error link-input-avatar-error'></span>
				</label>
				<button type='submit' className='button form__submit-button'>
					Сохранить
				</button>
			</PopupWithForm>

			<PopupWithForm
				title='Новое место'
				name='add-card'
				isOpen={isAddplaceOpen}
				onClose={closeAllPopups}
			>
				<label className='form__field'>
					<input
						type='text'
						className='form__input'
						id='place-name-input'
						name='name'
						placeholder='Название'
						minLength='2'
						maxLength='30'
						required
					/>
					<span className='form__error place-name-input-error'></span>
				</label>
				<label className='form__field'>
					<input
						type='url'
						className='form__input'
						id='link-input'
						name='link'
						placeholder='Ссылка на картинку'
						required
					/>
					<span className='form__error link-input-error'></span>
				</label>
				<button type='submit' className='button form__submit-button'>
					Создать
				</button>
			</PopupWithForm>

			<PopupWithForm
				title='Вы уверены?'
				name='confirm-delete'
				isOpen={isConfirmDeleteOpen}
				onClose={closeAllPopups}
			>
				<button type='submit' className='button form__confirm-button'>
					Да
				</button>
			</PopupWithForm>

			<ImagePopup onClose={closeAllPopups} card={selectedCard} />
		</>
	);
}

export default App;
